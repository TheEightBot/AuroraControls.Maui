namespace AuroraControls;

/// <summary>
/// Defines the shape options for step indicators.
/// </summary>
public enum StepIndicatorShape
{
    /// <summary>
    /// Circular shape (default).
    /// </summary>
    Circle,

    /// <summary>
    /// Square shape.
    /// </summary>
    Square,

    /// <summary>
    /// Rounded square shape.
    /// </summary>
    RoundedSquare,

    /// <summary>
    /// Diamond shape.
    /// </summary>
    Diamond,

    /// <summary>
    /// Hexagon shape.
    /// </summary>
    Hexagon,

    /// <summary>
    /// Triangle shape pointing upward.
    /// </summary>
    Triangle,

    /// <summary>
    /// Star shape (5-pointed).
    /// </summary>
    Star,

    /// <summary>
    /// Pentagon shape.
    /// </summary>
    Pentagon,

    /// <summary>
    /// Octagon shape.
    /// </summary>
    Octagon,
}
